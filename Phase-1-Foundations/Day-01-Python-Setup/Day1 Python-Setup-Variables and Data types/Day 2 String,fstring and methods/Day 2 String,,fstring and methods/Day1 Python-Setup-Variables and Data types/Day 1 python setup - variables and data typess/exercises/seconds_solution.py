""" Seconds in a day / week / year (solution).
"""

second_per_minute = 60
second_per_hour= 60*60
second_per_day= 24*second_per_hour

print("Seconds in per minute", second_per_minute)
print("Seconds in per day", second_per_day)
print("Second per week",  7*second_per_day)
print("Second per year", 365*second_per_day)